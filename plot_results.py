import csv
import argparse
import numpy as np
import matplotlib.pyplot as plt
import os


CSV_FILE = "controlled_density_edge_flip_results.csv"

SYSTEM_MATRIX_TYPE_COL_NUM = 3  # 1-based column number for system matrix type
GRAPH_TYPE_COL_NUM = 4  # 1-based column number for graph_type
DENSITY_COL_NUM = 7  # 1-based column number for original graph density
INPUT_MATRIX_TYPE_COL_NUM = 8  # 1-based column number for input matrix type
X_COL_NUM = 12  # 1-based column number for density_delta_from_avg

ALLOWED_SYSTEM_MATRIX_TYPES = {"adjacency", "neg_laplacian"}
ALLOWED_GRAPH_TYPES = {"connected_ER", "connected_RG", "BA"}
ALLOWED_INPUT_MATRIX_TYPES = {
    "identity",
    "all_ones",
    "zfs",
    "zfs_new",
    "zfs_transf",
}


def validate_input_matrix_types(input_matrix_types):
    """
    Validate requested input matrix types.

    Parameters
    ----------
    input_matrix_types : list[str], set[str], or None
        Requested input matrix types. If None, no input-matrix-type filtering
        is applied.

    Returns
    -------
    set[str] or None
        Set of input matrix types to include, or None if no filtering is requested.
    """
    if input_matrix_types is None:
        return None

    input_matrix_types = set(input_matrix_types)
    invalid_input_matrix_types = input_matrix_types - ALLOWED_INPUT_MATRIX_TYPES

    if invalid_input_matrix_types:
        raise ValueError(
            f"Invalid input_matrix_type value(s): "
            f"{sorted(invalid_input_matrix_types)}. "
            f"Allowed values are: {sorted(ALLOWED_INPUT_MATRIX_TYPES)}"
        )

    return input_matrix_types


def validate_density_range(density_range):
    """
    Validate density range.

    Parameters
    ----------
    density_range : list[float], tuple[float], or None
        Two values: minimum and maximum density.

    Returns
    -------
    tuple[float, float] or None
        Validated density range, or None if no density filtering is requested.
    """
    if density_range is None:
        return None

    if len(density_range) != 2:
        raise ValueError(
            "density_range must contain exactly two numbers: min_density max_density."
        )

    min_density = float(density_range[0])
    max_density = float(density_range[1])

    if min_density > max_density:
        raise ValueError(
            f"Invalid density_range: min_density={min_density} is greater than "
            f"max_density={max_density}."
        )

    return min_density, max_density


def validate_filter_values(filter_by, filter_values):
    """
    Validate requested filter values.

    Parameters
    ----------
    filter_by : str
        Either "graph_type" or "system_matrix_type".
    filter_values : list[str], set[str], or None
        Requested filter values. If None, all valid values for filter_by are used.

    Returns
    -------
    set[str]
        Set of filter values to include.
    """
    if filter_by == "graph_type":
        allowed_values = ALLOWED_GRAPH_TYPES
    elif filter_by == "system_matrix_type":
        allowed_values = ALLOWED_SYSTEM_MATRIX_TYPES
    else:
        raise ValueError(
            f"Invalid filter_by value: {filter_by}. "
            "Allowed values are: graph_type, system_matrix_type"
        )

    if filter_values is None:
        return allowed_values

    filter_values = set(filter_values)
    invalid_filter_values = filter_values - allowed_values

    if invalid_filter_values:
        raise ValueError(
            f"Invalid {filter_by} value(s): {sorted(invalid_filter_values)}. "
            f"Allowed values are: {sorted(allowed_values)}"
        )

    return filter_values


def clean_latex_label(label):
    """
    Convert labels like '$\\rho_S$' into '$|\\rho_S|$'.
    """
    label = label.strip()

    # Remove surrounding $ signs if present
    if label.startswith("$") and label.endswith("$"):
        label = label[1:-1]

    return rf"$|{label}|$"


def build_input_csv_files(csv_file, no_averaging=True, time_horizons=None):
    """
    Build the list of CSV files to load.

    If no_averaging is True, files are generated by inserting
    '_no_averaging_t_<horizon>' before the original file extension.

    Parameters
    ----------
    csv_file : str
        Base CSV filename.
    no_averaging : bool
        If True, load horizon-specific no-averaging files.
        If False, load csv_file directly.
    time_horizons : list[str] or None
        Time horizons used to construct no-averaging filenames.

    Returns
    -------
    list[str]
        List of CSV files to load.
    """
    if not no_averaging:
        return [csv_file]

    if time_horizons is None:
        time_horizons = ["0.01", "0.1", "1", "10"]

    csv_dir = os.path.dirname(csv_file)
    csv_basename = os.path.basename(csv_file)
    csv_stem, csv_ext = os.path.splitext(csv_basename)

    csv_files = []

    for horizon in time_horizons:
        horizon = str(horizon)
        no_averaging_basename = (
            f"{csv_stem}_no_averaging_t_{horizon}{csv_ext}"
        )
        no_averaging_file = os.path.join(csv_dir, no_averaging_basename)
        csv_files.append(no_averaging_file)

    return csv_files


def extract_column_data(
    csv_files,
    y_col_num,
    filter_by="graph_type",
    filter_values=None,
    no_averaging=True,
    density_range=None,
):
    """
    Extract x and y data from one or more CSV files.

    If no_averaging is True, y_col_num is treated as the actual coefficient
    column and no standard deviation column is loaded.

    If no_averaging is False, y_col_num is treated as the average column and
    the standard deviation is read from the column immediately after it.

    Rows can also be filtered by original graph density from column 7.

    Parameters
    ----------
    csv_files : str or list[str]
        One CSV file or multiple CSV files. If multiple files are provided,
        data are concatenated.
    y_col_num : int
        1-based column number containing the coefficient/average value to plot.
    filter_by : str
        Either "graph_type" or "system_matrix_type".
    filter_values : list[str], set[str], or None
        Values to include for the chosen filter.
    no_averaging : bool
        If True, do not load std values. If False, load std from y_col_num + 1.
    density_range : list[float], tuple[float], or None
        Optional density range [min_density, max_density]. If provided, only
        rows whose original graph density in column 7 lies within this range
        are loaded.

    Returns
    -------
    x_vals : np.ndarray
        Values from the x-axis column.
    y_vals_raw : np.ndarray
        Raw coefficient or average values from selected column.
    y_vals_abs : np.ndarray
        Absolute values for plotting.
    std_vals : np.ndarray or None
        Standard deviations if no_averaging is False, otherwise None.
    ylabel : str
        Matplotlib label based on previous column label.
    """
    filter_values = validate_filter_values(filter_by, filter_values)
    density_range = validate_density_range(density_range)

    if isinstance(csv_files, str):
        csv_files = [csv_files]

    x_idx = X_COL_NUM - 1
    y_idx = y_col_num - 1
    std_idx = y_idx + 1
    label_idx = y_idx - 1
    density_idx = DENSITY_COL_NUM - 1

    if filter_by == "graph_type":
        filter_idx = GRAPH_TYPE_COL_NUM - 1
    elif filter_by == "system_matrix_type":
        filter_idx = SYSTEM_MATRIX_TYPE_COL_NUM - 1
    else:
        raise ValueError(
            f"Invalid filter_by value: {filter_by}. "
            "Allowed values are: graph_type, system_matrix_type"
        )

    if y_col_num <= 1:
        raise ValueError(
            "y_col_num must be greater than 1 because the label "
            "is taken from the previous column."
        )

    x_vals = []
    y_vals_raw = []
    std_vals = []
    labels = []

    for csv_file in csv_files:
        if not os.path.exists(csv_file):
            raise FileNotFoundError(f"CSV file not found: {csv_file}")

        print(f"Loading data from: {csv_file}")

        with open(csv_file, newline="") as f:
            reader = csv.reader(f)

            for row in reader:
                required_max_idx = max(
                    x_idx,
                    filter_idx,
                    y_idx,
                    label_idx,
                    density_idx,
                )
                if no_averaging:
                    required_max_idx = max(
                        required_max_idx,
                        std_idx,
                    )

                if len(row) <= required_max_idx:
                    continue

                filter_value = row[filter_idx].strip()

                if filter_value not in filter_values:
                    continue

                try:
                    density = float(row[density_idx])
                except ValueError:
                    # Skips header row or rows with non-numeric density
                    continue

                if density_range is not None:
                    min_density, max_density = density_range

                    if density < min_density or density > max_density:
                        continue

                try:
                    x = float(row[x_idx])
                    y = float(row[y_idx])

                    if not no_averaging:
                        std = float(row[std_idx])
                except ValueError:
                    # Skips header row or rows where selected columns are not numeric
                    print(
                        f"Skipping row: {row} with non-numeric values at indices "
                        f"x={row[x_idx]}, y={row[y_idx]}, "
                        f"std={row[std_idx] if not no_averaging else 'N/A'}"
                    )
                    continue

                x_vals.append(x)
                y_vals_raw.append(y)

                if not no_averaging:
                    std_vals.append(std)

                labels.append(row[label_idx].strip())

    if not x_vals:
        density_filter_text = (
            f" and density_range={density_range}"
            if density_range is not None
            else ""
        )

        if no_averaging:
            raise ValueError(
                f"No numeric data found for coefficient column {y_col_num} "
                f"with {filter_by} filter {sorted(filter_values)}"
                f"{density_filter_text}. "
                f"Files checked: {csv_files}."
            )

        raise ValueError(
            f"No numeric data found for average column {y_col_num} "
            f"and std column {y_col_num + 1} "
            f"with {filter_by} filter {sorted(filter_values)}"
            f"{density_filter_text}. "
            f"Files checked: {csv_files}."
        )

    if no_averaging:
        print(
            f"Found {len(x_vals)} total data points for coefficient column "
            f"{y_col_num} with {filter_by} filter {sorted(filter_values)} "
            f"and density_range={density_range}."
        )
        std_vals = None
    else:
        print(
            f"Found {len(x_vals)} total data points for average column "
            f"{y_col_num} and std column {y_col_num + 1} "
            f"with {filter_by} filter {sorted(filter_values)} "
            f"and density_range={density_range}."
        )
        std_vals = np.asarray(std_vals, dtype=float)

    ylabel = clean_latex_label(labels[0])
    y_vals_raw = np.asarray(y_vals_raw, dtype=float)

    return (
        np.asarray(x_vals, dtype=float),
        y_vals_raw,
        np.abs(y_vals_raw),
        std_vals,
        ylabel,
    )


def make_scatter_plot(
    csv_file,
    y_col_nums,
    save_filename=None,
    filter_by="graph_type",
    filter_values=None,
    thresholds=None,
    y_label_text=None,
    threshold_line_mode="vertical",
    no_averaging=True,
    time_horizons=None,
    density_range=None,
):
    """
    Make scatter plots for multiple selected y-columns.

    If no_averaging is True, data are loaded from horizon-specific files and
    y_col_num is treated as the actual coefficient column. No std column is
    loaded, no error bars are plotted, and threshold reporting uses the
    coefficient value itself.

    If no_averaging is False, data are loaded from csv_file directly,
    y_col_num is treated as an average column, std is loaded from y_col_num + 1,
    error bars are plotted, and threshold reporting uses avg - std.

    Parameters
    ----------
    csv_file : str
        Base CSV file.
    y_col_nums : list[int]
        1-based column numbers containing coefficient or average values.
    save_filename : str or None
        If provided, save the figure to this filename.
    filter_by : str
        Either "graph_type" or "system_matrix_type".
    filter_values : list[str], set[str], or None
        Values to include for the chosen filter.
    thresholds : list[float], tuple[float], or None
        Thresholds used for reporting and threshold-line plotting.
    y_label_text : str or None
        Optional shared y-axis label text.
    threshold_line_mode : str
        Either "vertical" or "horizontal".
    no_averaging : bool
        If True, load no-averaging horizon files and do not use std columns.
    time_horizons : list[str] or None
        Time horizons used when no_averaging is True.
    """
    if y_label_text is None:
        y_label_text = ""
    else:
        y_label_text = y_label_text.replace("\\n", "\n")

    if threshold_line_mode not in {"vertical", "horizontal"}:
        raise ValueError(
            f"Invalid threshold_line_mode: {threshold_line_mode}. "
            "Allowed values are: vertical, horizontal"
        )

    csv_files = build_input_csv_files(
        csv_file,
        no_averaging=no_averaging,
        time_horizons=time_horizons,
    )

    print("\nCSV file(s) to be loaded:")
    for input_csv_file in csv_files:
        print(f"  {input_csv_file}")

    print(f"No averaging mode: {no_averaging}")
    
    if density_range is None:
        print("Density range filter: None")
    else:
        print(
            f"Density range filter: "
            f"{density_range[0]} <= density <= {density_range[1]}"
        )

    if thresholds is None:
        thresholds = [0.95, 0.8]

    thresholds = [float(threshold) for threshold in thresholds]

    if not thresholds:
        raise ValueError("At least one threshold must be provided.")

    graph_type_markers = {
        "connected_ER": "o",
        "connected_RG": "s",
        "BA": "^",
    }

    graph_type_colors = {
        "connected_ER": "tab:blue",
        "connected_RG": "tab:orange",
        "BA": "tab:purple",
    }

    system_matrix_type_markers = {
        "adjacency": "o",
        "neg_laplacian": "s",
    }

    system_matrix_type_colors = {
        "adjacency": "tab:blue",
        "neg_laplacian": "tab:red",
    }

    threshold_colors = {
        0.95: "green",
        0.8: "red",
    }

    if filter_by == "graph_type":
        filter_markers = graph_type_markers
        filter_colors = graph_type_colors
    elif filter_by == "system_matrix_type":
        filter_markers = system_matrix_type_markers
        filter_colors = system_matrix_type_colors
    else:
        raise ValueError(
            f"Invalid filter_by value: {filter_by}. "
            "Allowed values are: graph_type, system_matrix_type"
        )

    n_plots = len(y_col_nums)

    x_size_factor = 1.3
    y_size_factor = 1.5
    y_label_text_separate = True

    if n_plots == 1:
        y_size_factor = 2.5
        y_label_text_separate = False

    fig, axes = plt.subplots(
        n_plots,
        1,
        figsize=(x_size_factor * 3, y_size_factor * n_plots),
        sharex=True,
    )

    # Ensure axes is iterable for the single-subplot case
    if n_plots == 1:
        axes = [axes]

    legend_handles = []
    legend_labels = []

    for ax_idx, (ax, y_col_num) in enumerate(zip(axes, y_col_nums)):
        if no_averaging:
            print(f"\nSelected coefficient y-column number: {y_col_num}")
        else:
            print(f"\nSelected average y-column number: {y_col_num}")
            print(f"Selected std column number: {y_col_num + 1}")

        print(f"Selected filter mode: {filter_by}")
        print(f"Selected {filter_by} filter: {sorted(filter_values)}")
        print(f"Selected threshold(s): {thresholds}")
        print(f"Threshold line mode: {threshold_line_mode}")
        if density_range is not None:
            print(
                f"Selected density range: "
                f"{density_range[0]} <= density <= {density_range[1]}"
            )

        column_max_x_values_below_threshold = {
            threshold: [] for threshold in thresholds
        }

        ylabel = None

        for filter_value in sorted(filter_values):
            x_vals, y_vals_raw, y_vals_abs, std_vals, ylabel = extract_column_data(
                csv_files,
                y_col_num,
                filter_by=filter_by,
                filter_values=[filter_value],
                no_averaging=no_averaging,
                density_range=density_range,
            )

            if filter_by == "graph_type":
                legend_label = filter_value.replace("connected_", "")
            else:
                legend_label = filter_value

            print(f"\n{filter_by}: {filter_value}")

            if no_averaging:
                print("First 10 raw coefficient values of chosen y-column:")
                print(y_vals_raw[:10])
                threshold_check_vals = y_vals_abs
                threshold_check_label = "coefficient"
            else:
                print("First 10 raw average values of chosen y-column:")
                print(y_vals_raw[:10])
                print("First 10 standard deviation values:")
                print(std_vals[:10])
                threshold_check_vals = y_vals_abs - std_vals
                threshold_check_label = "avg - std"

            for threshold in thresholds:
                below_threshold_mask = threshold_check_vals < threshold
                x_vals_below_threshold = x_vals[below_threshold_mask]

                if len(x_vals_below_threshold) > 0:
                    max_x_below_threshold = np.max(x_vals_below_threshold)

                    column_max_x_values_below_threshold[threshold].append(
                        max_x_below_threshold
                    )

                    print(
                        f"Maximum x-axis value with {threshold_check_label} < "
                        f"{threshold}: {max_x_below_threshold}"
                    )

                    if threshold_line_mode == "vertical":
                        ax.axvline(
                            max_x_below_threshold,
                            color=filter_colors[filter_value],
                            linestyle="--",
                            linewidth=1,
                        )
                else:
                    print(
                        f"No points found with {threshold_check_label} < "
                        f"{threshold}"
                    )

            scatter = ax.scatter(
                x_vals,
                y_vals_abs,
                s=7,
                marker=filter_markers[filter_value],
                color=filter_colors[filter_value],
                label=legend_label,
            )

            if not no_averaging:
                ax.errorbar(
                    x_vals,
                    y_vals_abs,
                    yerr=std_vals,
                    fmt="none",
                    ecolor=filter_colors[filter_value],
                    elinewidth=0.5,
                    capsize=1.5,
                    capthick=0.5,
                    alpha=0.7,
                )

            # Add legend entries only once
            if ax_idx == 0:
                legend_handles.append(scatter)
                legend_labels.append(legend_label)

        if threshold_line_mode == "horizontal":
            for threshold in thresholds:
                ax.axhline(
                    threshold,
                    color=threshold_colors[threshold]
                    if threshold in threshold_colors
                    else "gray",
                    linestyle="--",
                    linewidth=1,
                )

        if no_averaging:
            summary_label = "coefficient"
            column_type_label = "coefficient y-column"
        else:
            summary_label = "avg - std"
            column_type_label = "average y-column"

        print(f"\nOverall summary for {column_type_label} {y_col_num}:")
        for threshold in thresholds:
            if len(column_max_x_values_below_threshold[threshold]) > 0:
                overall_max_x = np.max(
                    column_max_x_values_below_threshold[threshold]
                )

                print(
                    f"Overall maximum x-axis value with {summary_label} < "
                    f"{threshold}: {overall_max_x}"
                )
            else:
                print(
                    f"Overall: no points found with {summary_label} < "
                    f"{threshold}"
                )

        if y_label_text_separate:
            ax.set_ylabel(ylabel)
        else:
            ax.set_ylabel(y_label_text + "\n" + ylabel)

    axes[-1].set_xlabel("$\\delta_{\\text{max diff}}$")

    # One shared legend in a single row at the top of all plots
    fig.legend(
        legend_handles,
        legend_labels,
        loc="upper center",
        bbox_to_anchor=(0.5, 1.02),
        ncol=len(legend_labels),
        fontsize="small",
        frameon=False,
    )

    if y_label_text_separate:
        fig.supylabel(y_label_text, fontsize="medium")

    plt.tight_layout(rect=[0, 0, 1, 0.95])

    if save_filename:
        if density_range is not None:
            save_filename = save_filename.replace(".png", f"_orig_density_range_{density_range[0]}_to_{density_range[1]}.png")
        plt.savefig(save_filename, dpi=300, bbox_inches="tight")
        print(f"\nSaved plot to: {save_filename}")

    plt.show()


